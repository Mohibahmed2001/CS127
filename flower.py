import turtle

mohib = turtle.Turtle()
for i in range(36):
    mohib.forward(100)
    mohib.left(145)
    mohib.forward(10)
    mohib.right(90)
    mohib.forward(10)
    mohib.left(135)
    mohib.forward(100)
